package StreamAndOptional;

public class Osoba {
    String name;
    String lastName;
    int age;

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }

    public Osoba(String name, String lastName, int age) {
        this.name = name;
        this.lastName = lastName;
        this.age = age;


    }
}
